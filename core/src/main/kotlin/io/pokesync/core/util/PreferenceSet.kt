package io.pokesync.core.util

/**
 * Contains the user's preferences.
 * @author Sino
 */
class PreferenceSet {
    /**
     * A flag to indicate whether to display regulations such as
     * FPS-and Heap Usage.
     */
    var displayProfiling = true

    /**
     * A flag to indicate whether to render collision flags
     * onto the map as well.
     */
    var renderCollisionFlags = false
}